    public void user(int num, String us){
        us += ((int)(Math.random()*10));
        if(num>0)
            user(num-1, us);
        else
            reser.setUsuario(us);
    }
    public void pin(int num, String pi){
        pi += ((int)(Math.random()*10));
        if(num>0)
            pin(num-1, pi);
        else
            reser.setPin(pi);
    }


